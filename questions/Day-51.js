const colors = ['red', 'blue', 'green', 'yellow'];
const [z] = colors;

console.log(z);

// 👍A) 'red'
// 👏B) 'blue'
// 💡C) 'green'
// 😄D) 'yellow'

// Answer: A

// Explanation: There is an array colors containing four string elements.

// The destructuring assignment [z] is used to extract the first element of the array and assign it to the variable z.

// Therefore, z will hold the value of the first element of the array, which is 'red'.